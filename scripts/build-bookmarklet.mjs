// Builds the bookmarklet from bookmarklet.src.js into:
//   - bookmarklet.min.txt   (raw javascript: URL, handy for manual use)
//   - src/bookmarklet.js     (ES module exporting the URL for the React app)
// Run with: npm run bookmarklet
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

let src = readFileSync(join(root, "bookmarklet.src.js"), "utf8");
// strip block + line comments, collapse whitespace
src = src.replace(/\/\*[\s\S]*?\*\//g, "").replace(/^\s*\/\/.*$/gm, "");
src = src.replace(/\s*\n\s*/g, " ").replace(/\s{2,}/g, " ").trim();

const url = "javascript:" + encodeURIComponent(src);
if (url.includes("%00")) {
  console.error("ERROR: bookmarklet contains a null byte; aborting.");
  process.exit(1);
}

writeFileSync(join(root, "bookmarklet.min.txt"), url);
writeFileSync(
  join(root, "src", "bookmarklet.js"),
  `// AUTO-GENERATED by scripts/build-bookmarklet.mjs — do not edit by hand.\n` +
    `// Source of truth: bookmarklet.src.js\n` +
    `export const BOOKMARKLET = ${JSON.stringify(url)};\n`
);

console.log(`Bookmarklet built (${url.length} chars).`);
